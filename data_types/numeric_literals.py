a = 0b1010  # Binary Literals
b = 100  # Decimal Literal
c = 0o310  # Octal Literal
d = 0x12c  # Hexadecimal Literal

# Float Literal
float_1 = 10.5
float_2 = 1.5e2

# Complex Literal
x = 3.14j
y = 2.3 + 3.14j

print(a, b, c, d)
print(float_1, float_2)
print(x, x.imag, x.real)
print(y, y.imag, y.real)

print(x*y)
